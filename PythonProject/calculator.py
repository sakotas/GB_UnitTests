"""
Модуль для простых математических операций: сложение, вычитание, умножение и деление.
"""

def add(x, y):
    """Сложение x и y."""
    return x + y

def subtract(x, y):
    """Вычитание y из x с округлением результата до двух знаков после запятой."""
    return round(x - y, 2)

def multiply(x, y):
    """Умножение x на y."""
    return x * y

def divide(x, y):
    """Деление x на y с проверкой деления на ноль."""
    if y == 0:
        raise ValueError("Деление на ноль недопустимо")
    return x / y
